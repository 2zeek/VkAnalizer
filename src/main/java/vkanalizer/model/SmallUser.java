package vkanalizer.model;

import java.util.Objects;

/**
 * Created by Nikolay V. Petrov on 20.10.2017.
 */
public class SmallUser {

    private Integer id;
    private String firstName;
    private String lastName;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        SmallUser smallUser = (SmallUser) o;
        return Objects.equals(id, smallUser.id) &&
                Objects.equals(firstName, smallUser.firstName) &&
                Objects.equals(lastName, smallUser.lastName);
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", firstName='" + firstName + '\'' +
                ", lastName='" + lastName + '\'' +
                '}';
    }
}
